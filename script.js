function searchName() {
    const input = document.getElementById('searchInput').value.toLowerCase().trim();
    const resultsDiv = document.getElementById('searchResults');
    
    if (input.length < 2) {
        resultsDiv.style.display = "none";
        return;
    }

    resultsDiv.innerHTML = "";
    let matches = [];

    // Funksioni që ndërton rrugën (path) gjatë kërkimit
    function findDeep(list, currentPath = []) {
        list.forEach(p => {
            // Kontrollojmë emrin
            if (p.name.toLowerCase().includes(input)) {
                matches.push({
                    person: p,
                    path: [...currentPath] // Ruajmë gjithë listën e prindërve deri këtu
                });
            }
            // Nëse ka fëmijë, vazhdojmë kërkimin duke shtuar këtë person në rrugë
            if (p.children && p.children.length > 0) {
                findDeep(p.children, [...currentPath, p]);
            }
        });
    }

    findDeep(FAMILJET_MADHE);

    if (matches.length > 0) {
        resultsDiv.style.display = "block";
        matches.forEach(m => {
            const div = document.createElement('div');
            div.className = "search-item";
            
            // Shfaqim emrin e personit dhe emrin e babait në kllapa (nëse ka)
            const father = m.path.length > 0 ? m.path[m.path.length - 1].name : "Kryefamiljar";
            div.innerHTML = `<b>${m.person.name}</b> <small>(${father})</small>`;
            
            div.onclick = () => {
                // 1. Mbushim historinë me paraardhësit e vërtetë
                navigationHistory = m.path; 
                
                // 2. Hapim seksionin e trungut
                document.getElementById('home-section').style.display = "none";
                document.getElementById('family-selector').style.display = "none";
                document.getElementById('trungu-section').style.display = "flex";
                
                // 3. Shfaqim personin
                renderUI(m.person);
                
                // 4. Pastrojmë kërkimin
                resultsDiv.style.display = "none";
                document.getElementById('searchInput').value = "";
                window.scrollTo(0, 0);
            };
            resultsDiv.appendChild(div);
        });
    } else {
        resultsDiv.style.display = "none";
    }
}


